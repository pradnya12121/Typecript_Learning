export class Calculator1 {
    add(num1:number,num2:number){
        return num1+num2;
    }
    mul(num1:number,num2:number,num3:number){
        return num1*num2*num3
    }
    div(num1:number,num2:number){
        return num1/num2;
    }
    sub(num1:number,num2:number){
        return num1-num2;
    }
}